const somaArrow = (valor1, valor2) =>{
    console.log(valor1 + valor2);
}
somaArrow(10, 99);
somaArrow(10, 9);

//Subtração
const subArrow = (valor1, valor2) =>{
    console.log(valor1 - valor2);
}
somaArrow(10, 2);
somaArrow(10, 5);

//Mulriplicação
const multiArrow = (valor1, valor2) =>{
    console.log(valor1 * valor2);
}
somaArrow(10, 2);
somaArrow(10, 5);

//Divisão
const diviArrow = (valor1, valor2) =>{
    console.log(valor1 / valor2);
}
